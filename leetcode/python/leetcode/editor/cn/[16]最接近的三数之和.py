class Solution:
    def threeSumClosest(self, nums: List[int], target: int) -> int:
        """
        æ–¹æ³•1ï¼šæ’åºåï¼ŒåŒæŒ‡é’ˆ
        :param nums:
        :param target:
        :return:
        """
        ls = len(nums)
        sort_nums = sorted(nums)
        res = nums[0] + nums[1] + nums[2]
        for i in range(ls-2):
            j = i + 1
            k = ls -1
            while j < k:
                temp = sort_nums[i] + sort_nums[j] + sort_nums[k]
                if abs(target - temp) < abs(target - res):
                    res = temp
                if temp < target:
                    j += 1
                else:
                    k -= 1
        return res


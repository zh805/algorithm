# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def swapPairs(self, head: ListNode) -> ListNode:
        """
        æ–¹æ³•1ï¼šéå†
        :param head:
        :return:
        """
        cur = dummy = ListNode(0)
        dummy.next = head

        while cur.next and cur.next.next:
            node1 = cur.next
            node2 = cur.next.next
            node_next = node2.next

            node2.next = node1
            cur.next = node2
            node1.next = node_next

            cur = node1

        return dummy.next


        res = float('-inf')

        def traverse(root):
            if not root:
                return 0

            left = traverse(root.left)
            right = traverse(root.right)
            if left < 0:
                left = 0
            if right < 0:
                right = 0

            # å½“rootä¸ºæ ¹èŠ‚ç‚¹æ—¶çš„æœ€å¤§å€¼
